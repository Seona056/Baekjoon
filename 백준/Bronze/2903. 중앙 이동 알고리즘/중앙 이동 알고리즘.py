# 두 번째 
# 메모리 : 31120 KB, 시간 : 40 ms
# 소연님이 증가되는 수 a가 2의 거듭제곱인 것 같다고 하셔서 다시 추가한 내용
# 확실히 if문의 조건 하나가 줄어드니 시간이 줄어들었다.

n = int(input())
m, a = 2, 2

if n == 0:
	print(4)
else:
	s = [a**i for i in range(n)]
	print((m+sum(s))**2)


# 👇 else 부분만 고쳐봤음
n = int(input())
m, a = 2, 2

if n == 0:
	print(4)
else:
	for i in range(n):
		m += a**i
	print(m**2)



