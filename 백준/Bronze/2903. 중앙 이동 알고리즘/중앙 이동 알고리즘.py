# 세 번째 
# 메모리 : 31120 KB, 시간 : 40 ms
# index 0도 한번에 돌릴 수 있는 코드를 구상하다가 소연님이 푼 것을 조금 변형시켜보았다.
# n == 0이면 for문도 돌아가지 않음

n = int(input())

m = 2
for i in range(n):
	m += (2**i)	

print(m**2)
