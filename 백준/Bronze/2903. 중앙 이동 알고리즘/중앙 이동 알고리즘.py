# 첫 번째 답
# 메모리 : 31120 KB, 시간 : 44 ms
# 단순히 더해지는 수가 이전 증가분의 2배임을 이용하여 풀었다.
# 이 방법으로는 도저히 0번째와 첫번째를 같은 코드로 넣을 수가 없었다.

n = int(input())
m, a = 3, 1

if n == 0:
	print(4)
elif n == 1:
	print(9)
else:
	for i in range(n-1):
		a += a
		m += a
	print(m**2)



